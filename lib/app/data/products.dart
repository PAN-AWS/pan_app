// Semplice slugify per ID gruppo
String productKeyFromName(String name) =>
    name.toLowerCase().replaceAll('à', 'a').replaceAll('è', 'e')
        .replaceAll('é', 'e').replaceAll('ì', 'i').replaceAll('ò', 'o')
        .replaceAll('ù', 'u').replaceAll(RegExp(r'[^a-z0-9]+'), '_')
        .replaceAll(RegExp(r'_+'), '_').replaceAll(RegExp(r'^_|_$'), '');

